def caesar(text,shift,direction):
    end_text=""
    if direction=="decode":
        shift*=-1
    for letter in text:
        if letter in alphabet:
            position=alphabet.index(letter)
            new_position= position+shift
            end_text += alphabet[new_position]
        else:
            end_text+=letter
    print(f"The {direction}d text is {end_text}")

